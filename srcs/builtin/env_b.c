#include "../libft/libft.h"

int	env(int argc, char **argv, char **env)
{
	int	i;

	if (env == NULL)
		return (0);
	i = 0;
	while (env[i])
	{
		ft_putendl_fd(env[i], 1);
		i++;
	}
	return (0);
}
