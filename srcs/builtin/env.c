#include "core.h"

int	ft_env(char **args, t_term *t)
{
	char	**env;
	int		i;

	(void)args;
	(void)t;
	env = t->env;
	i = 0;
	while (env[i])
	{
		ft_putendl_fd(env[i], 1);
		i++;
	}
	return (0);
}
