#include "libft.h"

/*
** return: 1 if "c" is alpha, 0 if not
*/

int	ft_isalpha(char c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}
